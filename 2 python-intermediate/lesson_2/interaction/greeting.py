def hi(name):
    """Takes a string 'name', returns the greeting phrase with the name"""
    print(f"Hi {name}")


def hi_formal(name):
    """
    This is a reST style.
    The reStructuredText (reST) format that is used by Sphinx
    :param name: Name of the person
    :returns: formal greeting to the person
    """
    print(f"Hi Sir/Miss {name}")
